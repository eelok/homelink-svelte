    <h1>Was tun?</h1>
    <h2>Tagesausflüge und Exkursionen</h2>
    <ul>
		<li>
			<strong>Besuch in Meersburg:</strong> Macht eine Bootsfahrt nach Meersburg und erkundet die historische Stadt auf dem Hügel, umgeben von Weinbergen.
		</li>
		<li>
			<strong>Besuch in Konstanz:</strong> Macht eine längere Bootsfahrt nach Konstanz. Konstanz ist die größte Stadt in der Region und hat eine wunderschöne Altstadt mit reicher Geschichte. Es ist auch die größte Einkaufsstadt in der Region.
		</li>
		<li>
			<strong>Besuch in Bregenz:</strong> Bregenz ist die nächste Stadt zu Lindau in Österreich. Ihr könnt mit dem Boot oder dem Zug dorthin fahren. Eine Fahrradtour ist auch eine tolle Option und bietet einen wunderschönen Blick auf den See. In Bregenz könnt ihr mit der Pfänderbahn auf den Pfänderberg fahren und eine herrliche Aussicht auf den See und die umliegenden Berge genießen.
		</li>
		<li>
			<strong>Besuch in Friedrichshafen:</strong> Friedrichshafen ist bekannt für sein Zeppelin Museum und das Dornier Museum. Ihr könnt mit dem Boot oder dem Zug dorthin fahren.
		</li>
		<li>
			<strong>Besuch der Insel Mainau:</strong> Die Insel Mainau ist eine wunderschöne Blumeninsel im Bodensee. Ihr könnt mit dem Boot dorthin fahren.
		</li>
		<li>
			<strong>Besuch in Sankt Gallen:</strong> Sankt Gallen ist eine schöne Stadt in der Schweiz. Ihr könnt mit dem Zug dorthin fahren. Die Stadt ist bekannt für ihre wunderschöne Altstadt und die Abtei von Saint Gall mit ihrer fantastischen Bibliothek.
		</li>
		<li>
			<strong>Wellness:</strong> Besucht die
			<a href="https://www.therme-lindau.com/">Lindau-Therme</a> für ein entspannendes Spa-Erlebnis. Sie bietet eine Vielzahl von Saunen, Dampfbädern und Pools sowie einen großartigen Blick auf den See und die Berge.
		</li>        		
	</ul>
