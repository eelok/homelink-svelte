  <h1>Willkommen in unserer Wohnung!</h1>
  <p>
      Wir freuen uns sehr, dass ihr unsere Wohnung für euren Aufenthalt in Lindau gewählt habt. Wir hoffen, dass ihr euch hier genauso wohl fühlt wie wir und wünschen euch eine wunderbare Zeit am Bodensee!
  </p>
  <h3>Schlüssel</h3>
  <p>
      Es gibt zwei Schlüssel: einen für die Haustür und einen für die Wohnungstür. Der Schlüssel für die Wohnungstür hat ein rundes Ende, während der Schlüssel für die Haustür ein quadratisches Ende hat.
  </p>
  <h3>Wohnung</h3>
  <p>
      Unsere Wohnung befindet sich im zweiten Stock auf der rechten Seite. Bitte beachtet, dass das Gebäude keinen Aufzug hat, also gibt es ein paar Treppen zu steigen.
  </p>
  <p>Die Wohnung hat vier Zimmer. Folgendes steht euch zur Verfügung:</p>
  <ul>
      <li><strong>Wohnzimmer:</strong> mit Sofa, Fernseher, Schreibtisch und Stehlampe.</li>
      <li>
          <strong>Esszimmer:</strong> mit Esstisch, Radio, Kommode, Smartwatch/Lautsprecher und Saugroboter.
      </li>
      <li>
          <strong>Schlafzimmer:</strong> mit Doppelbett und Kleiderschrank. Wir räumen euch einen Teil des Kleiderschranks frei. Saubere Bettwäsche und 2 Handtuchsets liegen auf dem Bett bereit. Weitere Handtücher findet ihr in der Kommode im Esszimmer.
      </li>
      <li>
          <strong>Küche:</strong> ausgestattet mit Geschirrspüler, Waschtrockner und Kaffeemaschine. Ihr könnt gerne alle Zutaten für Müsli und alles im Kühlschrank benutzen. Bitte wascht Messer, Töpfe und Pfannen nicht im Geschirrspüler. Spülmittel für den Geschirrspüler und die Waschmaschine findet ihr im Vorratsschrank im Flur.
      </li>
      <li>
          <strong>Balkon:</strong> mit Tisch und zwei Stühlen. Im Winter müsst ihr die Pflanzen nicht gießen.
      </li>
      <li>
          <strong>Büro:</strong> Das Büro enthält einen Schreibtisch, einen Bürostuhl und eine Stehlampe. Der WLAN-Drucker, den ihr benutzen könnt, befindet sich im Flur.
      </li>
      <li>
          <b>Badezimmer und WC</b>: Zusätzliches Toilettenpapier findet ihr im Badezimmerschrank.
      </li>
      <li>
          <b>Flur:</b> Im Flur gibt es eine Garderobe, an der ihr eure Jacken aufhängen könnt, und einen Schuhschrank, in dem ihr eure Schuhe abstellen könnt. In der Garderobe findet ihr auch 2 Regenschirme, die ihr gerne benutzen könnt. Außerdem stehen euch ein Staubsauger und ein Wischmopp zur Verfügung. Bitte zieht eure Schuhe aus, wenn ihr die Wohnung betretet.
      </li>
  </ul>
  <h3>WLAN</h3>
  <p>
      Das WLAN-Netzwerk heißt <strong>Martin</strong>. Die Zugangsdaten findet ihr im Flur auf dem Telefonschrank.
  </p>
  <h3>Müll und Recycling</h3>
  <p>
      In der Küche gibt es 3 Mülleimer. Unter der Spüle gibt es zwei kleine Eimer für Biomüll und Restmüll. Es gibt 2 Eimer für Plastik und Papier, die separat in der Küche stehen. Ihr könnt die Müllbeutel in die großen Mülltonnen im Hof werfen. Es gibt Tonnen für Biomüll, Restmüll und Papier. Leider gibt es keine Tonne für Plastik, aber ihr könnt Plastikmüll ausnahmsweise in den Restmüll werfen oder in der Wohnung lassen.
  </p>
  <p>
      Im Hof neben dem Keller befinden sich die Mülltonnen in einem verschlossenen Raum. Ihr könnt den Raum mit dem Schlüssel von der Haustür öffnen.
  </p>
  <h3>Einkaufen & Essen</h3>
  <p>
      Zum Einkaufen empfehlen wir Netto am Marktplatz 5 oder Rewe in der Zeppelinstraße 6. Beide sind von 7 Uhr bis 20 Uhr geöffnet und sonntags sowie an Feiertagen geschlossen.
  </p>
  <p>
