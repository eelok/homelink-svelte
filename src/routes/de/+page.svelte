  <h3>Öffentliche Verkehrsmittel, Ausflüge und Sehenswürdigkeiten</h3>
  <p>
      Der Bahnhof Lindau-Insel ist nur 5 Gehminuten entfernt. Von dort gibt es Züge nach Friedrichshafen, München, Österreich und in andere Richtungen. In der Nähe befindet sich auch die Bushaltestelle Stadttheater. Am Hafen gibt es Boote nach Schweiz, Österreich und Rundfahrten, die leider im Winter nicht fahren.
  </p>
  <p>
      Wir empfehlen einen Ausflug nach Bregenz und von dort mit der Seilbahn auf den Pfänder. Von dort habt ihr einen großartigen Blick auf den Bodensee und die Berge.
  </p>
  <p>
      Meersburg ist auch eine schöne Stadt, die ihr mit öffentlichen Verkehrsmitteln oder mit dem Auto erreichen könnt.
  </p>
