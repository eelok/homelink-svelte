    <h1>Restaurants</h1>
    <h2>Lindau</h2>
    <p>Es gibt viele ausgezeichnete Restaurants auf <b>der Insel Lindau</b>. Du kannst eine Vielzahl auf Google Maps finden, aber hier sind einige unserer Favoriten.</p>
    <ul class="list-group list-group-flush">
		<li class="list-group-item">
			<a href="http://www.hamma.de/">Hamma Bäckerei</a> - Eine gute lokale Bäckerei mit einer Vielzahl von Broten und Gebäck, gelegen in der Maximilianstraße 11.
			<a href="https://maps.app.goo.gl/1EhnHmXCNYVgyHFC8" target="_blank">Auf der Karte ansehen</a>.
		</li>
        <li class="list-group-item">
            <a href="https://www.facebook.com/ThungthongThairestaurant.Lindau">Restaurant Thung Thong</a> - Großartiges Thai-Essen, bietet Mittagsmenüs und leckeres Pad Thai.
            <a href="https://maps.app.goo.gl/7DRsAMk5UZsdPvBj7" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.facebook.com/Pizzeria-Piccola-997046647026182/info/?tab=overview">Pizzeria & Eiscafé Piccola</a> - Eine wirklich gute Pizza für wenig Geld. Zum Mitnehmen und am Seeufer genießen.
            <a href="https://maps.app.goo.gl/hBZtB3ihRkHQysGJ7" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://streetkhazana.de/">Restaurant Street Khazana</a> - Dieses bietet indisches Street Food. Probier die Dosas, sie sind ihre Spezialität.
            <a href="https://maps.app.goo.gl/ow6AwjtQjF3NDupAA" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://vinahaus-lindau.de/">Restaurant Vina Haus</a> - Das ist unser absoluter Favorit. Sie bieten eine Vielzahl von vietnamesischen Gerichten, darunter Pho und Bun. Sie haben wirklich gute Angebote für das Mittagessen unter der Woche.
            <a href="https://maps.app.goo.gl/iegYJTQZngEBwTdF8" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://engel-lindau.de/gast-und-bierstube/">Restaurant Engel</a> - Ideal für diejenigen, die regionale Küche erleben möchten, gelegen in der Schafgasse 4.
            <a href="https://maps.app.goo.gl/iSXYMAJwFPc2FynA9" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.restaurant-zum-alten-rathaus-lindau.de/">Restaurant zum Alten Rathaus</a> - Ein weiteres traditionelles Restaurant, gelegen in der Salzgasse 2.
            <a href="https://maps.app.goo.gl/3mRvCqvTbpmRuhS66" target="_blank">Auf der Karte ansehen</a>.
        </li>
    </ul>
    <h2>Bregenz</h2>
    <p>Wenn ihr euch in <b>Bregenz</b> befindet, solltet ihr diese hoch empfohlenen Restaurants in Betracht ziehen:</p>
    <ul class="list-group list-group-flush">
        <li class="list-group-item">
            <a href="https://www.sakurasushi.at/">Sakura Sushi Bar</a> - Eines der besten Sushi-Restaurants in der Region. Probiert die "Dragon Roll" oder "Rainbow Roll".
            <a href="https://maps.app.goo.gl/gBjeAyWZDUTP3Fpy7" target="_blank">Auf der Karte ansehen</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.hotelweisseskreuz.at/goldener-hirschen/">Goldener Hirschen</a> - Wenn ihr ein traditionelles österreichisches Restaurant sucht, ist dies der richtige Ort. Käsespätzle ist ein Muss.
            <a href="https://maps.google.com?q=Bregenz,Goldener%20Hirschen" target="_blank">Auf der Karte ansehen</a>.
        </li>
		<li class="list-group-item">
			<a href="http://bahi.at/">BAHI café & coworking</a> - Der beste Kaffee in Bregenz.
			<a href="https://maps.app.goo.gl/dn7Ta7vgLUXmaNds7" target="_blank">Auf der Karte ansehen</a>.
		</li>
		<li class="list-group-item">
			<a href="https://www.xn--bckerei-mangold-0kb.at/">Bäckerei Mangold</a> - Es ist eine Bäckerei, also erwartet keinen guten Kaffee dort. Aber sie haben einige einzigartige Gebäckstücke. Probiert das Bienenstichcroissant.
			<a href="https://maps.app.goo.gl/aPtAM2LuuHY113rLA" target="_blank">Auf der Karte ansehen</a>.
		</li>
    </ul>
