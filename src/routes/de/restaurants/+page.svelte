	<h1>Restaurants</h1>
	<h2>Restaurants in Lindau</h2>
	<p>Es gibt viele gute Restaurants auf der <b>Lindau-Insel</b>.</p>
	<ul class="list-group list-group-flush">
		<li class="list-group-item"><a href="https://www.facebook.com/ThungthongThairestaurant.Lindau">Restaurant Thung Thong</a>. 
			Im Nachbarhaus gibt es ein gutes thailändisches Restaurant Thung Thong, das viele köstliche Thaigerichte anbietet.
		</li>
		<li class="list-group-item"><a href="https://il-leone-pizza-bar.webador.de/">Pizzeria IL Leone</a>. 
			Die Pizzeria IL Leone in der Vordere Metzgergasse 14 macht leckere Pizzen.
		</li>
		<li class="list-group-item"><a href="https://streetkhazana.de/">Restaurant Street Khazana</a>. 
			Wir empfehlen auch das indische Restaurant Street Khazana (Schmiedgasse 8).
		</li>
		<li class="list-group-item"><a href="https://vinahaus-lindau.de/">Restaurant Vina Haus</a>. 
			Das vietnamesische Restaurant Vina Haus (Langenweg 24). Nicht auf der Insel, aber in der Nähe.
		</li>
		<li class="list-group-item"><a href="https://engel-lindau.de/gast-und-bierstube/">Restaurant Engel</a>. 
			Wenn ihr die regionale Küche probieren möchtet, empfehlen wir euch das Restaurant Engel (Schafgasse 4).
		</li>
		<li class="list-group-item"><a href="https://www.restaurant-zum-alten-rathaus-lindau.de/">Restaurant zum Alten Rathaus</a>. 
			Das Restaurant zum Alten Rathaus (Salzgasse 2) ist auch sehr gut.
		</li>
	</ul>
