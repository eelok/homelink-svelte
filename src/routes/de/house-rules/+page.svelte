    <h1>Hausregeln und Pflegehinweise</h1>
    <ul>
      <li>
        <strong>Rauchen ist in der Wohnung und auf dem Balkon nicht erlaubt.</strong>
        <p>Dies sorgt für eine saubere, geruchsfreie Umgebung für alle Gäste und verhindert Rauchschäden an Vorhängen, Möbeln und Wänden. Es trägt auch zur guten Luftqualität in der Wohnung bei.</p>
      </li>
      <li>
        <strong>Schuhe beim Betreten der Wohnung ausziehen.</strong>
        <p>Dies hilft, die Böden sauber zu halten, reduziert den Verschleiß der Holzoberflächen und sorgt für eine hygienische Wohnumgebung.</p>
      </li>
      <li>
        <strong>Keine Vögel füttern oder Essen auf dem Balkon lassen.</strong>
        <p>Das Füttern zieht Vögel an, die regelmäßig zurückkehren könnten, was zu Kot auf dem Balkon führt. Dies kann unschöne Flecken, Hygieneprobleme und mögliche Schäden an der Balkonoberfläche verursachen.</p>
      </li>
      <li>
        <strong>Die Kaffeemaschine (Brühgruppe, Milchaufschäumer und Milchbehälter) nach jedem Gebrauch reinigen.</strong>
        <p>Regelmäßige Reinigung sorgt für die Langlebigkeit der Maschine, erhält die Qualität des Kaffees und verhindert das Wachstum von Bakterien oder Schimmel.</p>
      </li>
      <li>
        <strong>Keine Essensreste in der Küchenspüle entsorgen. Benutze den Biomüllbehälter für Essensreste.</strong>
        <p>Dies verhindert Verstopfungen der Rohre, unangenehme Gerüche und mögliche Sanitärprobleme, die teuer zu reparieren sein könnten.</p>
      </li>
      <li>
        <strong>Beim Beladen der Spülmaschine Essensreste zuerst in den Biomüllbehälter kratzen.</strong>
        <p>Dies sorgt für eine effektivere Reinigung und verhindert, dass Speisereste den Spülmaschinenfilter verstopfen.</p>
      </li>
      <li>
        <strong>Keine Messer, Töpfe und Pfannen in der Spülmaschine waschen. Diese Gegenstände von Hand spülen.</strong>
        <p>Spülmaschinen können die Oberfläche dieser Gegenstände beschädigen und Messerklingen stumpf machen. Eisenpfannen rosten sofort nach dem Spülen in der Spülmaschine. Handwäsche sorgt für ihre Langlebigkeit und richtige Pflege.</p>
      </li>
      <li>
        <strong>Den bereitgestellten Anweisungen zur richtigen Mülltrennung und -entsorgung folgen.</strong>
        <p>Richtige Abfallwirtschaft ist entscheidend für die Umweltverträglichkeit und die Einhaltung lokaler Vorschriften. Sie trägt auch zur Sauberkeit in der Wohnung und den umliegenden Bereichen bei.</p>
      </li>
      <li>
        <strong>Alle Fenster schließen, wenn du die Wohnung verlässt und bei Regenwetter.</strong>
        <p>Dies verhindert Wasserschäden und sorgt für Sicherheit.</p>
      </li>
      <li>
        <strong>Partys sind nicht erlaubt.</strong>
        <p>Dies sorgt für eine ruhige Umgebung für die Nachbarn und verhindert mögliche Schäden an der Wohnung.</p>
      </li>
      <li>
        <strong>Bitte die Pflanzen gießen, wenn die Erde trocken ist.</strong>
        <p>Es wäre toll, wenn ihr unsere Pflanzen während eures Aufenthalts vor dem Austrocknen retten könntet.</p>
      </li>
      <li>
        <strong>Die Wohnung in dem Zustand hinterlassen, in dem ihr sie bei der Ankunft vorgefunden habt.</strong>
        <p>Dies beinhaltet das Reinigen der Oberflächen, das Zurückstellen der Gegenstände an ihren ursprünglichen Platz, das Ausschalten der Heizung und das Melden von versehentlichen Schäden. Dies erleichtert uns die Rückkehr und erhält das Vertrauen innerhalb der Homelink-Community.
            Für eure Bequemlichkeit haben wir eine <a href="/de/departure">Abreise-Checkliste</a> vorbereitet, damit ihr nichts vergesst.
        </p>
      </li>
    </ul>
  </div>