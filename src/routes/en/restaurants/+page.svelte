    <h1>Restaurants</h1>
    <h2>Lindau</h2>
    <p>There are many excellent restaurants on <b>Lindau Island</b>. You can find a variety on Google Maps, but here are some of our favourites.</p>
    <ul class="list-group list-group-flush">
		<li class="list-group-item">
			<a href="http://www.hamma.de/">Hamma Bäckerei</a> - A good local bakery with a variety of breads and pastries located on  Maximilianstraße 11.
			<a href="https://maps.app.goo.gl/1EhnHmXCNYVgyHFC8" target="_blank">View on map</a>.
		</li>
        <li class="list-group-item">
            <a href="https://www.facebook.com/ThungthongThairestaurant.Lindau">Restaurant Thung Thong</a> - Great Thai food, offering lunch menus and delicious Pad Thai.
            <a href="https://maps.app.goo.gl/7DRsAMk5UZsdPvBj7" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.facebook.com/Pizzeria-Piccola-997046647026182/info/?tab=overview">Pizzeria & Eiscafé Piccola</a> - A really good quality pizza for little money. Take away and it at the lake side.
            <a href="https://maps.app.goo.gl/hBZtB3ihRkHQysGJ7" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://streetkhazana.de/">Restaurant Street Khazana</a> - This one offers some Indian Street Food. Try Dosas, they are their speciality.
            <a href="https://maps.app.goo.gl/ow6AwjtQjF3NDupAA" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://vinahaus-lindau.de/">Restaurant Vina Haus</a> - This one is our most favourite. They offer a variety of Vietnamese dishes, including Pho and Bun. They have really good offers for lunch during the week.
            <a href="https://maps.app.goo.gl/iegYJTQZngEBwTdF8" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://engel-lindau.de/gast-und-bierstube/">Restaurant Engel</a> - Ideal for those wanting to experience regional cuisine, located on Schafgasse 4.
            <a href="https://maps.app.goo.gl/iSXYMAJwFPc2FynA9" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.restaurant-zum-alten-rathaus-lindau.de/">Restaurant zum Alten Rathaus</a> - Another traditional restaurant located on Salzgasse 2.
            <a href="https://maps.app.goo.gl/3mRvCqvTbpmRuhS66" target="_blank">View on map</a>.
        </li>
    </ul>
    <h2>Bregenz</h2>
    <p>If you find yourself in <b>Bregenz</b>, consider these highly recommended dining options:</p>
    <ul class="list-group list-group-flush">
        <li class="list-group-item">
            <a href="https://www.sakurasushi.at/">Sakura Sushi Bar</a> - One of the best sushi restaurants in the region. Try the "Dragon Roll." or "Rainbow Roll."
            <a href="https://maps.app.goo.gl/gBjeAyWZDUTP3Fpy7" target="_blank">View on map</a>.
        </li>
        <li class="list-group-item">
            <a href="https://www.hotelweisseskreuz.at/goldener-hirschen/">Goldener Hirschen</a> - If you are looking for a traditional Austrian restaurant, this is the place to go. Käsespätzle is a must-try.
            <a href="https://maps.google.com?q=Bregenz,Goldener%20Hirschen" target="_blank">View on map</a>.
        </li>
		<li class="list-group-item">
			<a href="http://bahi.at/">BAHI café & coworking</a> - The best coffee in Bregenz.
			<a href="https://maps.app.goo.gl/dn7Ta7vgLUXmaNds7" target="_blank">View on map</a>.
		</li>
		<li class="list-group-item">
			<a href="https://www.xn--bckerei-mangold-0kb.at/">Backery Mangold</a> - It is a bakery, so don't expect a good coffee there. But they have some unique pastries. Try the Bienenstichcroissant.
			<a href="https://maps.app.goo.gl/aPtAM2LuuHY113rLA" target="_blank">View on map</a>.
		</li>
    </ul>
