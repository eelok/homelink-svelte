    <h1>Restaurants</h1>
    <h2>Restaurants in Lindau</h2>
    <p>There are many good restaurants on <b>Lindau Island</b>.</p>
    <ul class="list-group list-group-flush">
        <li class="list-group-item"><a href="https://www.facebook.com/ThungthongThairestaurant.Lindau">Restaurant Thung Thong</a>. 
            In the neighboring building, there is a good Thai restaurant, Thung Thong, which offers many delicious Thai dishes.
        </li>
        <li class="list-group-item"><a href="https://il-leone-pizza-bar.webador.de/">Pizzeria IL Leone</a>. 
            The Pizzeria IL Leone on Vordere Metzgergasse 14 makes delicious pizzas.
        </li>
        <li class="list-group-item"><a href="https://streetkhazana.de/">Restaurant Street Khazana</a>. 
            We also recommend the Indian restaurant Street Khazana (Schmiedgasse 8).
        </li>
        <li class="list-group-item"><a href="https://vinahaus-lindau.de/">Restaurant Vina Haus</a>. 
            The Vietnamese restaurant Vina Haus (Langenweg 24). Not on the island, but nearby.
        </li>
        <li class="list-group-item"><a href="https://engel-lindau.de/gast-und-bierstube/">Restaurant Engel</a>. 
            If you want to try regional cuisine, we recommend the Restaurant Engel (Schafgasse 4).
        </li>
        <li class="list-group-item"><a href="https://www.restaurant-zum-alten-rathaus-lindau.de/">Restaurant zum Alten Rathaus</a>. 
            The Restaurant zum Alten Rathaus (Salzgasse 2) is also very good.
        </li>
    </ul>
    <h2>Restaurants in Bregenz</h2>
    <p>If you are in <b>Bregenz</b>, we recommend the following restaurants:</p>
    <ul class="list-group list-group-flush">
        <li class="list-group-item">
            <a href="https://www.sakurasushi.at/">Sakura Sushi Bar</a>. A restaurant with Japanese cuisine.
        </li>
        <li class="list-group-item">
            <a href="https://www.hotelweisseskreuz.at/goldener-hirschen/">Goldener Hirschen</a>. A traditional restaurant with Austrian cuisine.
        </li>
    </ul>
