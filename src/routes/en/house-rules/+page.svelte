    <h1>House Rules and Care Instructions</h1>
    <ul>
      <li>
        <strong>No smoking in the apartment or on the balcony.</strong>
        <p>This ensures a clean, odor-free environment for all guests and prevents smoke damage to curtains, furniture, and walls. It also helps maintain good air quality within the apartment.</p>
      </li>
      <li>
        <strong>Remove shoes when entering the apartment.</strong>
        <p>This helps keep the floors clean, reduces wear and tear on the wooden floor surfaces, and maintains a hygienic living environment.</p>
      </li>
      <li>
        <strong>Do not feed birds or leave food on the balcony.</strong>
        <p>Feeding attracts birds that may return regularly, leading to droppings on the balcony. This can cause unsightly stains, hygiene issues, and potential damage to the balcony surface.</p>
      </li>
      <li>
        <strong>Clean the coffee machine (brew group, milk frother, and milk container) after each use.</strong>
        <p>Regular cleaning ensures the longevity of the machine, maintains the quality of the coffee, and prevents the growth of bacteria or mold.</p>
      </li>
      <li>
        <strong>Do not dispose of food residues in the kitchen sink. Use the organic waste bin for food scraps.</strong>
        <p>This prevents clogging of the pipes, unpleasant odors, and potential plumbing issues that could be costly to repair.</p>
      </li>
      <li>
        <strong>When loading the dishwasher, scrape food residues into the organic waste bin first.</strong>
        <p>This ensures more effective cleaning and prevents food particles from clogging the dishwasher filter.</p>
      </li>
      <li>
        <strong>Do not wash knives, pots, and pans in the dishwasher. Hand wash these items instead.</strong>
        <p>Dishwashers can damage the finish of these items and dull knife blades. Iron pans will immediately rust after washing in a dishwasher. Hand washing ensures their longevity and proper care.</p>
      </li>
      <li>
        <strong>Follow the provided instructions for proper waste separation and disposal.</strong>
        <p>Proper waste management is crucial for environmental sustainability and compliance with local regulations. It also helps maintain cleanliness in the apartment and surrounding areas.</p>
      </li>
      <li>
        <strong>Close all windows when leaving the apartment and during rainy weather.</strong>
        <p>This prevents water damage and maintains security.</p>
      </li>
      <li>
        <strong>Parties are not allowed.</strong>
        <p>This ensures a peaceful environment for neighbors and prevents potential damage to the apartment.</p>
      </li>
      <li>
        <strong>Please water the plants if the soil is dry.</strong>
        <p>Would be great if you could save our plants from drying out during your stay.</p>
      </li>
      <li>
        <strong>Leave the apartment in the same condition you found it upon arrival.</strong>
        <p>This includes cleaning the surfaces, returning items to their original places, turning off the heating, and reporting any accidental damage. This makes it easier for us on our return and maintains trust within the Homelink community.
            For your convenience we prepared a <a href="/en/departure">departure checklist</a> to ensure you don't forget anything.
        </p>
      </li>
    </ul>
